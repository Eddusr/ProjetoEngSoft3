    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (tfcadConfsenha.getText().compareTo(tfcadSenha.getText()) != 0) {
            jLabel5.setText("As senhas n√£o conferem");
            tfcadConfsenha.setText("");
            jLabel5.setVisible(true);
        }
        
        else {
             ControlUser control = new ControlUser();
             if (control.cadUser(tfcadLogin.getText(), tfcadSenha.getText(), Integer.parseInt(tfcadPerfil.getText()))){
                 jLabel5.setText("Cadastro realizado com sucesso.");
                 jLabel5.setVisible(true);
                 tfcadLogin.setText("");
                 tfcadSenha.setText("");
                 tfcadConfsenha.setText("");
                 tfcadPerfil.setText("");
             }
             else
                JOptionPane.showMessageDialog(null, "Algo deu errado, verifique o login e senha.", "Erro", JOptionPane.ERROR_MESSAGE);

        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

