    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        MaterialDAO dao = new MaterialDAO("mysql");
        Material mat = new Material();
        mat.setCodigo(Integer.parseInt(tfCod.getText()));
        mat.setNome(tfcadNome.getText());
        if (dao.alterar(mat)){
            DefaultTableModel dtmMateriais = (DefaultTableModel) tableMateriais.getModel();
            Object[] dados = {tfCod.getText(), tfcadNome.getText()};
            dtmMateriais.insertRow(tableMateriais.getSelectedRow(), dados);
            dtmMateriais.removeRow(tableMateriais.getSelectedRow() + 1);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

