    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Material material = new Material();
        MaterialDAO dao = new MaterialDAO("mysql");

        material.setNome(tfcadNome.getText());

        if (!dao.save(material)) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar material.");
        } else {
            MaterialDAO dao2 = new MaterialDAO("mysql");
            //Colocando Codigo e NOME do ultimo material cadastrado na Tabela e limpando os campos
            DefaultTableModel dtmMateriais = (DefaultTableModel) tableMateriais.getModel();
            Object[] dados = {dao2.getCod(material.getNome()),tfcadNome.getText()};
            tableMateriais.setVisible(true);
            dtmMateriais.addRow(dados);
            
            tfcadNome.setText(null);
            //jLabel5.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        if (tableMateriais.getSelectedRow() != -1){
            DefaultTableModel dtmPacientes = (DefaultTableModel) tableMateriais.getModel();
            MaterialDAO dao = new MaterialDAO("mysql");
            if (dao.deletar(Integer.parseInt(tfCod.getText()))){
                dtmPacientes.removeRow(tableMateriais.getSelectedRow());
            }
            else
                JOptionPane.showMessageDialog(null, "Falha ao deletar");
        }
        else
        JOptionPane.showMessageDialog(null, "Nenhum registo selecionado.");
        
    }//GEN-LAST:event_jButton2ActionPerformed

