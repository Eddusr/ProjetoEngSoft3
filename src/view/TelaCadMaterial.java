    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        MaterialDAO dao = new MaterialDAO("mysql");
        List<Material> materiais = dao.selecionar();
        
        DefaultTableModel dtmMateriais = (DefaultTableModel) tableMateriais.getModel();
        for (Material m : materiais){
            Object[] dados = {m.getCodigo(), m.getNome()};
            dtmMateriais.addRow(dados);
        }
        tableMateriais.setVisible(true);
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void tableMateriaisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMateriaisMouseClicked

        if (tableMateriais.getSelectedRow() != -1){
            tfCod.setText(tableMateriais.getValueAt(tableMateriais.getSelectedRow(), 0).toString());
            tfcadNome.setText(tableMateriais.getValueAt(tableMateriais.getSelectedRow(), 1).toString());
            
        }

    }//GEN-LAST:event_tableMateriaisMouseClicked

